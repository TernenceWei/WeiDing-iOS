#import "UILabel+WS.h"
#import "UIFont+WS.h"

@implementation UILabel (WS)

+ (UILabel *)labelWithText:(NSString *)text textColor:(UIColor *)textColor fontSize:(float)fontSize{
    
    UILabel *label = [[UILabel alloc] init];
    label.textAlignment = NSTextAlignmentLeft;
    label.text = text;
    label.textColor =textColor;
    label.font = [UIFont WSFontOfSize:fontSize];
    label.numberOfLines = 0;
    return label;
}
@end
